// Função utilitária para filtrar depoimentos por rating
export function filterByRating(testimonials, minRating = 4) {
  return testimonials.filter(testimonial => 
    testimonial.rating && testimonial.rating >= minRating
  );
}

// Função utilitária para embaralhar depoimentos
export function shuffleTestimonials(testimonials) {
  const shuffled = [...testimonials];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

// Função utilitária para selecionar depoimentos aleatórios
export function getRandomTestimonials(testimonials, count = 5) {
  const shuffled = shuffleTestimonials(testimonials);
  return shuffled.slice(0, count);
}

// Função utilitária para agrupar depoimentos por empresa
export function groupByCompany(testimonials) {
  return testimonials.reduce((groups, testimonial) => {
    const company = testimonial.company || 'Sem empresa';
    if (!groups[company]) {
      groups[company] = [];
    }
    groups[company].push(testimonial);
    return groups;
  }, {});
}

